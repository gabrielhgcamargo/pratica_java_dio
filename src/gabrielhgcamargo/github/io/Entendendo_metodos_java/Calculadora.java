package gabrielhgcamargo.github.io.Entendendo_metodos_java;

// Classe com os m√©todos da Calculadora
public class Calculadora {

    public static Integer soma(Integer valor1, Integer valor2){
        return valor1 + valor2;
    }

    public static Integer subtracao(Integer valor1, Integer valor2){
        return valor1 - valor2;
    }

    public static Integer multiplicacao(Integer valor1, Integer valor2){
        return valor1 * valor2;
    }

    public static Float divide(Float valor1, Float valor2){
        return valor1 / valor2;
    }
}
